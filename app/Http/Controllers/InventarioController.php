    public function stock()
    {
        //
        $stock = \DB::table('invetarios')
        ->join('materia_prima','invetarios.id_p','=','materia_prima.id')
        ->select('materia_prima.producto AS producto','invetarios.cantidad AS cantidad')
        ->paginate(15);

        if(count($stock) == 0){
            return \Redirect::back()->withErrors('No existe stock!!!');
        }

        return view('admin.stock',['mensaje'=>'Existencia activa', 'stocks'=>$stock]);
    }
